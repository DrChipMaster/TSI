package com.drchip.ihelp;

public class Post {
    public String Author;
    public String Description;
    public String Title;
    public String ImagePath;

    public Post() {

    }

    public Post(String author, String description, String title, String imagePath) {
        Author = author;
        Description = description;
        Title = title;
        ImagePath = imagePath;
    }
}
